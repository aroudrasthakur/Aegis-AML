class TestCreateSARPDF:
    """Tests for create_sar_pdf method."""

    def test_returns_pdf_bytes(self, pdf_generator, sample_sar_data):
        """Test that PDF generation returns bytes."""
        result = pdf_generator.create_sar_pdf(sample_sar_data)

        assert isinstance(result, bytes)
        assert len(result) > 0

    def test_pdf_starts_with_pdf_header(self, pdf_generator, sample_sar_data):
        """Test that generated bytes start with PDF header."""
        result = pdf_generator.create_sar_pdf(sample_sar_data)

        # PDF files start with %PDF-
        assert result.startswith(b"%PDF-")

    def test_generates_valid_pdf_structure(self, pdf_generator, sample_sar_data):
        """Test that generated PDF has valid structure."""
        result = pdf_generator.create_sar_pdf(sample_sar_data)

        # Check for PDF EOF marker
        assert b"%%EOF" in result

    def test_handles_short_narrative(self, pdf_generator, sample_sar_data):
        """Test PDF generation with short narrative."""
        sample_sar_data.narrative = "Short narrative."
        
        result = pdf_generator.create_sar_pdf(sample_sar_data)

        assert isinstance(result, bytes)
        assert len(result) > 0

    def test_handles_long_narrative(self, pdf_generator, sample_sar_data):
        """Test PDF generation with long narrative that spans multiple pages."""
        # Create a long narrative (approximately 2000 words)
        long_narrative = " ".join(["This is a test sentence."] * 400)
        sample_sar_data.narrative = long_narrative
        
        result = pdf_generator.create_sar_pdf(sample_sar_data)

        assert isinstance(result, bytes)
        assert len(result) > 0
        # Longer narrative should produce larger PDF
        assert len(result) > 3000

    def test_handles_empty_narrative(self, pdf_generator, sample_sar_data):
        """Test PDF generation with empty narrative."""
        sample_sar_data.narrative = ""
        
        result = pdf_generator.create_sar_pdf(sample_sar_data)

        assert isinstance(result, bytes)
        assert len(result) > 0


