class TestPDFProperties:
    """Property-style tests for PDF correctness guarantees."""

    def test_property_7_pdf_uses_letter_dimensions(self, pdf_generator):
        from reportlab.lib.pagesizes import letter
        assert (pdf_generator.page_width, pdf_generator.page_height) == letter

    def test_property_8_pdf_handles_multi_page_narrative(self, pdf_generator, sample_sar_data):
        PyPDF2 = pytest.importorskip("PyPDF2")
        sample_sar_data.narrative = " ".join(["Suspicious activity details"] * 4000)
        pdf_bytes = pdf_generator.create_sar_pdf(sample_sar_data)
        reader = PyPDF2.PdfReader(BytesIO(pdf_bytes))
        assert len(reader.pages) >= 2

    def test_property_9_pdf_round_trip_validity(self, pdf_generator, sample_sar_data):
        PyPDF2 = pytest.importorskip("PyPDF2")
        pdf_bytes = pdf_generator.create_sar_pdf(sample_sar_data)
        reader = PyPDF2.PdfReader(BytesIO(pdf_bytes))
        assert len(reader.pages) >= 1

    def test_property_32_pdf_section_completeness(self, pdf_generator, sample_sar_data):
        PyPDF2 = pytest.importorskip("PyPDF2")
        pdf_bytes = pdf_generator.create_sar_pdf(sample_sar_data)
        reader = PyPDF2.PdfReader(BytesIO(pdf_bytes))
        text = "\n".join((p.extract_text() or "") for p in reader.pages)
        assert "Subject Information" in text
        assert "Reporting Institution Details" in text
        assert "Suspicious Activity Details" in text
        assert "Narrative" in text

    def test_special_characters_are_sanitized(self, pdf_generator, sample_sar_data):
        sample_sar_data.narrative = "Bad\x00Narrative\\with\fcontrols"
        out = pdf_generator.create_sar_pdf(sample_sar_data)
        assert out.startswith(b"%PDF-")
